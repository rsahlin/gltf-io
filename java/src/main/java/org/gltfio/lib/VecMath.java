
package org.gltfio.lib;

public abstract class VecMath {

    /**
     * Index to x axis. Used to determine order when multiple axes are used.
     */
    public static final int X = 0;

    /**
     * Index to y axis. Used to determine order when multiple axes are used.
     */
    public static final int Y = 1;

    /**
     * Index to z axis. Used to determine order when multiple axes are used.
     */
    public static final int Z = 2;

}
